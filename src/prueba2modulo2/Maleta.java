package prueba2modulo2;

/**
 *
 * @author davidbousquet
 */
public class Maleta {
    int idMaleta, peso, dimension, pasajero;
    String equipajeEspecial;

    public Maleta() {
    }

    public int getIdMaleta() {
        return idMaleta;
    }

    public void setIdMaleta(int idMaleta) {
        this.idMaleta = idMaleta;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }

    public int getDimension() {
        return dimension;
    }

    public void setDimension(int dimension) {
        this.dimension = dimension;
    }

    public int getPasajero() {
        return pasajero;
    }

    public void setPasajero(int pasajero) {
        this.pasajero = pasajero;
    }

    public String getEquipajeEspecial() {
        return equipajeEspecial;
    }

    public void setEquipajeEspecial(String equipajeEspecial) {
        this.equipajeEspecial = equipajeEspecial;
    }

    
}
